//------------------------------------------------------------------------------------------------
class ACE_Math
{
	//------------------------------------------------------------------------------------------------
	static float Exp(float x)
	{
		return Math.Pow(Math.E, x);
	}
	
	//------------------------------------------------------------------------------------------------
	static float Hill(float x)
	{
		return x / (1 + x);
	}
	
	//------------------------------------------------------------------------------------------------
	static float Round(float value, int precision)
	{
		float scale = Math.Pow(10, precision);
		return Math.Round(value * scale) / scale;
	}
}
