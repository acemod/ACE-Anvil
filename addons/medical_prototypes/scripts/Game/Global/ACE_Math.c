//------------------------------------------------------------------------------------------------
class ACE_Math
{
	//------------------------------------------------------------------------------------------------
	static float Exp(float x)
	{
		return Math.Pow(Math.E, x);
	}
	
	//------------------------------------------------------------------------------------------------
	static float Hill(float x)
	{
		return x / (1 + x);
	}
	
	//------------------------------------------------------------------------------------------------
	static float AbsHill(float x)
	{
		return x / (1 + Math.AbsFloat(x));
	}
}
